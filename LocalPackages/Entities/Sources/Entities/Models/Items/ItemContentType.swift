import Foundation

/// Use the raw values for any telemetry monitoring
public enum ItemContentType: Int, CaseIterable, Equatable, Sendable, Hashable, Codable {
    case login = 0
    case alias = 1
    case note = 2
    case creditCard = 3
    case identity = 4
    case sshKey = 5
    case wifi = 6
    case custom = 7
}

extension ItemContentType: CustomDebugStringConvertible {
    public var debugDescription: String {
        switch self {
        case .login:
            "login"
        case .alias:
            "alias"
        case .note:
            "note"
        case .creditCard:
            "creditCard"
        case .identity:
            "identity"
        case .sshKey:
            "sshkey"
        case .wifi:
            "wifi"
        case .custom:
            "custom"
        }
    }
}
